# Meminta pengguna untuk memasukkan suhu dalam Celsius
celsius = float(input("Masukkan suhu dalam derajat Celsius: "))

# Menghitung konversi dari Celsius ke Fahrenheit
fahrenheit = (celsius * 9/5) + 32

# Menampilkan hasil konversi
print(f"{celsius} derajat Celsius sama dengan {fahrenheit} derajat Fahrenheit")

